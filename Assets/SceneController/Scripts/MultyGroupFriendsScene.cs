                var edgeInfos = new List<EdgeSerializationInfo>();
                var vertexInfos = new List<VertexSerializationInfo>();

                for (var k = 0; k < groupCount; ++k)
                {
                    groups.Add(new List<VertexSerializationInfo>(manCount));
                    for (var i = 0; i < UnityEngine.Random.Range(3, manCount + 1); ++i)
                    {
                        placeholderEnum.MoveNext();
                        groups[k].Add(new VertexSerializationInfo(placeholderEnum.Current, 10, names[UnityEngine.Random.Range(0, names.Length)], Color.white, Guid.NewGuid().ToString()));
                        vertexInfos.Add(groups[k][groups[k].Count - 1]);
                    }

                    var isLocalConnected = new bool[manCount, manCount];

                    for (var i = 0; i < groups[k].Count; i++)
                    {
                        for (var j = 0; j < UnityEngine.Random.Range(3, sibManCount + 1); ++j)
                        {
                            var randId = UnityEngine.Random.Range(0, groups[k].Count);
                            if (!isLocalConnected[i, randId] && randId != i)
                            {
                                isLocalConnected[i, randId] = true;
                                isLocalConnected[randId, i] = true;
                                edgeInfos.Add(new EdgeSerializationInfo(groups[k][i].Id, groups[k][randId].Id, null, true, 1, Color.red, new SpringParameters(50, 150)));
                            }
                        }
                    }
                }

                var isConnected = new Dictionary<string, HashSet<string>>();

                for (var i = 0; i < groups.Count; ++i)
                {
                    for (var j = 0; j < UnityEngine.Random.Range(1, sibGroupCount + 1); ++j)
                    {
                        var groupRandomIndex = i;
                        while (groupRandomIndex == i)
                            groupRandomIndex = UnityEngine.Random.Range(0, groups.Count);
                        for (var k = 0; k < UnityEngine.Random.Range(2, sibGroupManCount + 1); ++k)
                        {
                            var fistId = groups[i][UnityEngine.Random.Range(0, groups[i].Count)].Id;
                            var secondId = groups[groupRandomIndex][UnityEngine.Random.Range(0, groups[groupRandomIndex].Count)].Id;
                            if (!isConnected.ContainsKey(fistId))
                                isConnected[fistId] = new HashSet<string>();
                            if (!isConnected[fistId].Contains(secondId))
                            {
                                isConnected[fistId].Add(secondId);
                                if (!isConnected.ContainsKey(secondId))
                                    isConnected[secondId] = new HashSet<string>();
                                isConnected[secondId].Add(fistId);
                                edgeInfos.Add(new EdgeSerializationInfo(fistId, secondId, null, true, 1, Color.blue, new SpringParameters(50, 2500)));
                            }
                        }
                    }
                }

